package LearnJavaPackage;

public class PredefinedMathClass
{

	public static void main(String[] args)
{
		System.out.println (Math.addExact(250, 250) ); //add methods
		
		System.out.println (Math.addExact(1234564654556l, 123646546545456l) );
		
		System.out.println(Math.subtractExact(500, 300)); //subtract methods
		
		System.out.println(Math.subtractExact(97687687687884l, 454684768468l));
		
		System.out.println(Math.multiplyExact(25, 25)); //multiply methods
		
		System.out.println(Math.multiplyExact(4565465465l, 451554));
		System.out.println(Math.multiplyFull(9848, 8746));
		
		System.out.println(Math.divideExact(650, 25)); //division methods
		
		System.out.println(Math.max(68456, 35465)); // prints the maximum number
		
		System.out.println(Math.min(684545, 54545)); // prints the minimum number
		
		System.out.println(Math.abs(-850545)); // prints -ve number into +ve number
		
		System.out.println(Math.PI); //prints the value of pi
		
		System.out.println(Math.random()); //generates random value between 0 & 1

}

}
